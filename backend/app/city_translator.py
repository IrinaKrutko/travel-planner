def get_coordinates(city: str) -> tuple:
    """
    Возвращает координаты (широта, долгота) для города.
    Если город не найден, возвращает None.
    """
    city_lower = city.lower().strip()
    
    # Прямое совпадение
    if city_lower in CITY_COORDINATES:
        return CITY_COORDINATES[city_lower]
    
    # Частичное совпадение (например, "санкт петербург" → найдёт "санкт-петербург")
    for name, coords in CITY_COORDINATES.items():
        if city_lower in name or name in city_lower:
            return coords
    
    # Если не нашли — возвращаем None
    return None

